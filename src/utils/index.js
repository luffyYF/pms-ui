/**
 * 初始化间隔多少天的时间数组
 * @param day
 * @returns {{}}
 */
export function initDate (day) {
  let now = new Date()
  let data = {}
  data.end = formatDate(now, 'yyyy-MM-dd')
  now.setDate(now.getDate() - day + 1)
  data.begin = formatDate(now, 'yyyy-MM-dd')
  return data
}

export function getBetweenDay (begin, end) {
  begin = new Date(begin)
  end = new Date(end)
  let days = end.getTime() - begin.getTime()
  let time = parseInt(days / (1000 * 60 * 60 * 24)) + 1
  let array = []
  for (let i = 0; i < time; i++) {
    array[i] = formatDate(begin, 'yyyy-MM-dd')
    begin.setDate(begin.getDate() + 1)
  }
  return array
}

//在原有日期基础上，增加days天数，默认增加1天
export function addDate (dateStr, days) {
  if (days == undefined || days == '') {
    days = 1;
  }
  let date = new Date(dateStr);
  date.setDate(date.getDate() + days);
  let month = date.getMonth() + 1;
  let day = date.getDate();
  return date.getFullYear() + '-' + getFormatDate(month) + '-' + getFormatDate(day);
}
 // 日期月份/天的显示，如果是1位数，则在前面加上'0'
function getFormatDate(arg) {
  if (arg == undefined || arg == '') {
    return '';
  }
  var re = arg + '';
  if (re.length < 2) {
    re = '0' + re;
  }
  return re;
}

export function formatDate (date, fmt) {
  if (/(y+)/.test(fmt)) {
    fmt = fmt.replace(RegExp.$1, (date.getFullYear() + '').substr(4 - RegExp.$1.length))
  }
  let o = {
    'M+': date.getMonth() + 1,
    'd+': date.getDate(),
    'h+': date.getHours(),
    'm+': date.getMinutes(),
    's+': date.getSeconds()
  }
  for (let k in o) {
    if (new RegExp(`(${k})`).test(fmt)) {
      let str = o[k] + ''
      fmt = fmt.replace(RegExp.$1, (RegExp.$1.length === 1) ? str : padLeftZero(str))
    }
  }
  return fmt
}

